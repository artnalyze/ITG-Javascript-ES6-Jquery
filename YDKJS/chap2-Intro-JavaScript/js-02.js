/**
 * string
 * number
 * boolean
 * null and undefined
 * object
 * symbol (new to ES6)
 */
var a ;
console.log(typeof a);

a = "Hello";
console.log(typeof a);

a = 42;
console.log(typeof a);

a = true;
console.log(typeof a);

a = null;
console.log(typeof a);

a = undefined;
console.log(typeof a);

a = {b: "c"};
console.log(typeof a);

